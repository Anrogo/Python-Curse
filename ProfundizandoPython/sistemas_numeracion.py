# Convertir a un tipo entero, incluyendo base:
# Base Decimal
a = int('23', 10)

# Base Binario
a = int('10111', 2)

# Base Octal
a = int('27', 8)

# Base Hexadecimal
a = int('17', 16)

# Base 5
a = int('43', 5)
print(f'a: {a}')